import { FilterPanel } from '@/components/shared/FilterPanel'
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from '@/components/ui/select'
import { Input } from '@/components/ui/input'
import { emptyReceiptEntryFilters, hasActiveReceiptEntryFilters } from '../lib/receiptEntryFilters'
import type { DocumentStatus, ReceiptEntryFilterValues } from '../types'

const ALL = '__all__'

interface ReceiptEntryFiltersBarProps {
  value: ReceiptEntryFilterValues
  onChange: (value: ReceiptEntryFilterValues) => void
}

/** Same server-side shape as PaymentEntryFiltersBar — values are sent straight to the server. */
export function ReceiptEntryFiltersBar({ value, onChange }: ReceiptEntryFiltersBarProps) {
  return (
    <FilterPanel onClear={() => onChange(emptyReceiptEntryFilters)} hasActiveFilters={hasActiveReceiptEntryFilters(value)}>
      <div className="flex flex-col gap-1.5">
        <span className="text-xs text-muted-foreground">Status</span>
        <Select
          value={value.status ?? ALL}
          onValueChange={(next) => onChange({ ...value, status: next === ALL ? null : (next as DocumentStatus) })}
        >
          <SelectTrigger className="w-40">
            <SelectValue placeholder="All statuses" />
          </SelectTrigger>
          <SelectContent>
            <SelectItem value={ALL}>All statuses</SelectItem>
            <SelectItem value="draft">Draft</SelectItem>
            <SelectItem value="submitted">Submitted</SelectItem>
          </SelectContent>
        </Select>
      </div>
      <div className="flex flex-col gap-1.5">
        <span className="text-xs text-muted-foreground">From</span>
        <Input
          type="date"
          className="w-40"
          value={value.dateFrom}
          onChange={(event) => onChange({ ...value, dateFrom: event.target.value })}
        />
      </div>
      <div className="flex flex-col gap-1.5">
        <span className="text-xs text-muted-foreground">To</span>
        <Input
          type="date"
          className="w-40"
          value={value.dateTo}
          onChange={(event) => onChange({ ...value, dateTo: event.target.value })}
        />
      </div>
    </FilterPanel>
  )
}
